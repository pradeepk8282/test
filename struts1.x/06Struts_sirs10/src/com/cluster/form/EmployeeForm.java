package com.cluster.form;

import org.apache.struts.validator.ValidatorForm;

public class EmployeeForm extends ValidatorForm {
	
	private String name;
	private int id;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
}
